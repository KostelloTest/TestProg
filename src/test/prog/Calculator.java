package test.prog;

public class Calculator {
    Calculator() {

    }

    protected double sum(double a, double b) {
        return a + b;
    }

    protected double mul(double a, double b) {
        return a * b;
    }

    protected double div(double a, double b) {
        return a / b;
    }

    protected double sub(double a, double b) {
        return a - b;
    }

}
